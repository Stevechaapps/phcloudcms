import { readdirSync, readFileSync, writeFileSync, existsSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const themesDir = join(__dirname, '..', 'src', 'themes');
const indexPath = join(themesDir, 'index.ts');

const files = readdirSync(themesDir)
  .filter(f => f.endsWith('.ts') && f !== 'index.ts' && f !== 'example.ts')
  .sort();

const imports = files.map(f => `import './${f.replace(/\.ts$/, '.js')}';`).join('\n');

const header = `// Auto-generated by scripts/generate-theme-index.js
// DO NOT EDIT — add theme .ts files to src/themes/ and rebuild.

`;

if (files.length === 0) {
  writeFileSync(indexPath, header + `// No themes found in src/themes/\n`);
} else {
  writeFileSync(indexPath, header + imports + '\n');
}

console.log(`✓ Generated theme index — ${files.length} theme(s) found`);
if (files.length > 0) console.log(`  ${files.join(', ')}`);
